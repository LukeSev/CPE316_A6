#include "main.h"
ADC_HandleTypeDef hadc1;
USART_HandleTypeDef husart2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_Init(void);
static void MX_ADC1_Init(void);

uint8_t flag = 0;				// Flag for ISR
uint16_t digital = 0;			// What the ADC reads in

int main(void)
{
	HAL_Init();
	SystemClock_Config();
	MX_GPIO_Init();
	MX_USART2_Init();
	MX_ADC1_Init();

	// enable clocks
	RCC->AHB2ENR |= (RCC_AHB2ENR_GPIOAEN); 		// enable clock for Tx and Rx
	RCC->APB1ENR1 |= (RCC_APB1ENR1_USART2EN); 	// enable USART2 clock
	RCC->CR |= (RCC_CR_PLLSAI1ON);				// enable ADC clock

	__enable__irq();

	uint16_t inputs[20];						// Input array to save data
	uint8_t i = 0;

	// Start a conversion
	ADC1->CR |= (ADC_CR_ADSTART);

	while (1)
	{
		// Fill array with measured data points
		while(i < 20)
		{
			while(!flag);						// Spin while waiting for flag
			inputs[i] = digital;
			flag = 0;							// Clear flag
			// Start another conversion
			ADC1->CR |= (ADC_CR_ADSTART);
			i++;
		}
	}
}



// ISR's
void USART2_IRQHandler(void)
{
	if (USART2->ISR & USART_ISR_RXNE)
	{
		// flag is cleared by reading from USART_RDR
		char c = USART2->RDR;				// read from USART_RDR
		if (c == R)							// if R is pressed
			UART_ESC_Code("[31m");			// change text to red
		else if (c == G)					// if G is pressed
			UART_ESC_Code("[32m");			// change text to green
		else if (c == B)					// if B is pressed
			UART_ESC_Code("[36m");			// change text to blue
		else if (c == W)					// if W is pressed
			UART_ESC_Code("[37m");			// change text to white
		else
		{
			char str[2] = "\0";
			str[0] = c;
			UART_print(str);
		}
	}
	else if (USART2->ISR & USART_ISR_ORE)	// overrun error
	{
		USART2->ICR &= ~(USART_ICR_ORECF);	// clear flag
	}
}

void ADC1_2_IRQHandler(void)
{
	while(ADC1->CR & ADC_CR_ADSTART);		// Wait for conversion to complete

	// Read data in from data reg
	digital = (uint16_t)(ADC1->DR);			// Save data in global var
	flag = 1;
}

